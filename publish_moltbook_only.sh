JSON=$(jq -n \
  --arg c "$CONTENT" \
  --arg t "الظلم → العدل" \
  --arg s "islam_ai_ethics" \
  --arg n "Abdullah_Haqq" \
  '{content:$c, title:$t, submolt:$s, submolt_name:$n}')

